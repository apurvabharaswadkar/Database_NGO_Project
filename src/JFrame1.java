    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat format =new SimpleDateFormat("yyyy-MM-dd");
        Date start_date = jXDatePicker1.getDate();
        String start_date_str = format.format(start_date);

        Date end_date = jXDatePicker2.getDate();
        String end_date_str = format.format(end_date);

        Setvisibility();
        jTable4.setVisible(true);
        model.setRowCount(0);
        String[] columnNames ={"Course Name","Start Date","Center Name","Center Location","State","Teachers"};
        model.setColumnCount(6);
        model.setColumnIdentifiers(columnNames);

        Connection con = ConnectionManager.getConnection();
        try {
            CallableStatement statement = con.prepareCall(("call GetScheduleInfobyDate(?,?);"));
            statement.setString(1,start_date_str);
            statement.setString(2,end_date_str);
            ResultSet resultSet = statement.executeQuery();
            while(resultSet.next())
            {
                String Column1 = resultSet.getString("course.Course_Name");
                String Column2 = resultSet.getString("schedule.Start_Date");
                String Column3 = resultSet.getString("center.Center_Name");
                String Column4 = resultSet.getString("center.Center_Location");
                String Column5 = resultSet.getString("center.State");
                String Column6 = resultSet.getString("Teacher_Name");
                model.addRow(new Object[]{Column1, Column2,Column3,Column4,Column5,Column6});
            }
        }
        catch(Exception e)
        {
            System.err.println("Exception:" + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

